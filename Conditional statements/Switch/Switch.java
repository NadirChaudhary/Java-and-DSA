public class Switch{
    public static void main(String args[]){
        char ch = 'D';
        switch(ch){
            case 'A' : System.out.println("samosa");
            break;
            case 'B' : System.out.println("burger");
            break;
            case 'C' : System.out.println("mango shake");
            break;
            default : System.out.println("we wake up");
        }
    }
}