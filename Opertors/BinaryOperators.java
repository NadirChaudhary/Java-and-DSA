public class BinaryOperators{
    public static void main(String args[]){
        int a = 10;
        int b = 5;

        // Addition
        int add = a+b;
        System.out.println(add);

        // Subtraction
        int sub = a-b;
        System.out.println(sub);

        // Multiplication
        int mul = a*b;
        System.out.println(mul);

        // Divide
        int div = a/b;
        System.out.println(div);

        // Reminder(modulo)
        int mod = a%b;
        System.out.println(mod);
    }
}