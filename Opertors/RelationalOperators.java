public class RelationalOperators{
    public static void main(String args[]){
        int a = 19;
        int b = 15;

       // Equal
       System.out.println(a==b);

       // not equal
       System.out.println(a!=b);

       // Greater
       System.out.println(a>b);

       // Less
       System.out.println(a<b);

       // Greater and equal
       System.out.println(a>=b);

       // Less and equal
       System.out.println(a<=b);
    }
}